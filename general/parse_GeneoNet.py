def parse_pocket_text(text: str) -> None:
    """ Function for parsing raw GeneoNet output
    to create a .csf file for pipeline.
    """
    # split by pocket headers
    records = []
    lines = [l.strip() for l in text.splitlines() if l.strip()!='']
    idxs = [i for i,l in enumerate(lines) if re.fullmatch(r'\d+', l)]
    for i, start in enumerate(idxs):
        end = idxs[i+1] if i+1 < len(idxs) else len(lines)
        block = lines[start:end]
        pocket = int(block[0])
        cur = 1

        def get_float_at(cur):
            try:
                return float(block[cur]), cur+1
            except:
                return None, cur+1

        def parse_bracket(s):
            m = re.search(r'\[([^\]]+)\]', s)
            if not m:
                return None
            parts = [float(p) for p in re.split(r',\s*', m.group(1).strip())]
            return parts

        # convert lists to strings for .csv
        def list_to_str(lst):
            if lst is None:
                return ''

            return '[' + ', '.join(f'{x:.3f}' for x in lst) + ']'

        volume, cur = get_float_at(cur)
        score, cur = get_float_at(cur)
        small = ''
        if cur < len(block) and block[cur].upper() == 'X':
            small = 'X'
            cur += 1

        center = None
        radius = None
        range_x = None
        range_y = None
        range_z = None

        # find center: first item from cur that contains "[" and 3 numbers
        while cur < len(block) and center is None:
            if "[" in block[cur]:
                center = parse_bracket(block[cur])

            cur += 1
        for j in range(cur-1, len(block)):
            try:
                val = float(block[j])
                if j>1:
                    radius = val
                    cur = j+1
                    break
            except:
                continue

        for j in range(cur, len(block)):
            if range_x is None and '[' in block[j]:
                range_x = parse_bracket(block[j])
                continue
            if range_y is None and '[' in block[j]:
                range_y = parse_bracket(block[j])
                continue
            if range_z is None and '[' in block[j]:
                range_z = parse_bracket(block[j])
                continue

        rec = {
            'Pocket': pocket,
            'Volume': volume,
            'Score': score,
            'Small': small,
            'Center': list_to_str(center),
            'Radius': radius,
            'Range X': list_to_str(range_x),
            'Range Y': list_to_str(range_y),
            'Range Z': list_to_str(range_z)
        }
        records.append(rec)
    df = pd.DataFrame(records).sort_values('Pocket').reset_index(drop=True)
    return df

